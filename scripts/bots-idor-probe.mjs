#!/usr/bin/env node
// @ts-check
/**
 * Anon-read IDOR regression guard (bot idea #4).
 *
 * Hits PostgREST with the PUBLIC anon key (the one embedded in the browser
 * bundle) and asserts that sensitive tables return ZERO rows to anon — i.e. RLS
 * still denies anonymous reads. This guards every anon-read leak closed in
 * #1410 / #1431 / #1432: if a future migration re-opens a `USING (true)` policy
 * (or a new table ships world-readable), this fails.
 *
 * A table "leaks" when the anon GET returns >= 1 row. An empty array (RLS-denied
 * or genuinely empty) passes. This is the same methodology as the manual
 * `SET ROLE anon` probe, runnable in CI with only the public anon key.
 *
 *   NEXT_PUBLIC_SUPABASE_URL=… NEXT_PUBLIC_SUPABASE_ANON_KEY=… npm run bots:idor
 *   BOTS_IGNORE_HTTPS_ERRORS=1 …                               # sandbox proxy
 *
 * Skips clean (exit 0) when the env isn't configured, so it never breaks a
 * fork/clone. Exit 1 when any sensitive table exposes a row to anon.
 * Cost: $0 — no browser, no LLM.
 */
import { request } from "playwright";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const ANON = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;
const IGNORE_TLS = process.env.BOTS_IGNORE_HTTPS_ERRORS === "1";
const today = new Date().toISOString().slice(0, 10);
const reportPath = path.join(process.cwd(), "bots", "reports", `idor-${today}.md`);

// Tables that MUST be denied to the anon role (PII / secrets / financial /
// own-data). Mirrors the locked set from the RLS remediations + known-sensitive.
const SENSITIVE = [
  "advisor_applications", "advisor_bookings", "advisor_auth_tokens", "api_keys",
  "user_portfolios", "data_license_subscribers", "lead_disputes", "advisor_billing",
  "fee_alert_subscriptions", "broker_wallets", "wallet_transactions",
  "marketplace_invoices", "quiz_leads", "email_captures", "professional_leads",
  "leads", "consultation_bookings", "advisor_credit_ledger", "advisor_sessions",
  "investor_goals", "property_holdings", "business_accounts",
];

async function main() {
  if (!URL || !ANON) {
    console.log("[bots:idor] NEXT_PUBLIC_SUPABASE_URL / ANON_KEY not set — skipping (clean).");
    process.exit(0);
  }
  await fs.mkdir(path.dirname(reportPath), { recursive: true });
  const ctx = await request.newContext({
    ignoreHTTPSErrors: IGNORE_TLS,
    timeout: 20_000,
    extraHTTPHeaders: { apikey: ANON, Authorization: `Bearer ${ANON}` },
  });

  const rows = [];
  for (const table of SENSITIVE) {
    process.stdout.write(`[bots:idor] ${table} … `);
    let verdict = "ok", visible = 0, status = 0;
    try {
      const res = await ctx.get(`${URL}/rest/v1/${table}?select=*&limit=1`);
      status = res.status();
      if (status === 200) {
        const body = await res.json().catch(() => []);
        visible = Array.isArray(body) ? body.length : 0;
        if (visible > 0) verdict = "LEAK";
      } // 401/403/404 → anon denied/absent → ok
    } catch {
      verdict = "error";
    }
    process.stdout.write(`${verdict === "LEAK" ? "🔴 LEAK" : verdict === "error" ? "… err" : "✓"} (${status})\n`);
    rows.push({ table, verdict, visible, status });
  }
  await ctx.dispose();

  const leaks = rows.filter((r) => r.verdict === "LEAK");
  const lines = [
    `# Anon-read IDOR guard — ${today}`, "",
    `Target: \`${URL}\` (anon key) · $0.`, "",
    leaks.length === 0
      ? `## ✅ No sensitive table exposes rows to anon (${rows.length} probed).`
      : `## 🔴 ${leaks.length} sensitive table(s) LEAK to anon.`, "",
    ...(leaks.length ? ["### Leaks", ...leaks.map((l) => `- 🔴 \`${l.table}\` — anon sees ${l.visible}+ row(s)`), ""] : []),
    "<details><summary>All probed tables</summary>", "",
    ...rows.map((r) => `- ${r.verdict === "LEAK" ? "🔴" : "✅"} \`${r.table}\` (HTTP ${r.status})`),
    "", "</details>", "", "---", "_Generated by `npm run bots:idor`._",
  ];
  await fs.writeFile(reportPath, lines.join("\n") + "\n");
  console.log(`\n[bots:idor] Report: ${path.relative(process.cwd(), reportPath)}`);
  console.log(`[bots:idor] ${leaks.length === 0 ? "CLEAN" : leaks.length + " LEAK(S)"} (${rows.length} probed)`);
  process.exit(leaks.length > 0 ? 1 : 0);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch((err) => { console.error("[bots:idor] fatal:", err); process.exit(2); });
}
