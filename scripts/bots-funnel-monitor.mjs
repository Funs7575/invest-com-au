#!/usr/bin/env node
// @ts-check
/**
 * Synthetic-user funnel uptime monitor (bot idea #2).
 *
 * Probes the revenue-critical funnel steps as a synthetic user and validates
 * the RESPONSE SHAPE, not just the status — so a "200 but empty/broken payload"
 * (e.g. a phantom-table 500 swallowed into [], or a renamed field) is caught.
 *
 * Only non-mutating steps are probed: page GETs + the pure compute endpoints
 * (/api/quiz/data, /api/quiz/score, /api/wealth-stack). Lead/enquiry/booking
 * WRITES are never called — no junk data, no side effects, no firewall needed.
 *
 *   npm run bots:funnel
 *   BOTS_BASE_URL=https://my-preview.vercel.app npm run bots:funnel
 *   BOTS_IGNORE_HTTPS_ERRORS=1 npm run bots:funnel   # sandbox TLS-MITM proxy
 *
 * Output: bots/reports/funnel-YYYY-MM-DD.md + console summary.
 * Exit: 0 all steps healthy · 1 a step failed (consistently, after retries).
 * Cost: $0 — Playwright request client, no browser, no Anthropic calls.
 */
import { request } from "playwright";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const BASE = (process.env.BOTS_BASE_URL || "https://lambent-sawine-17c3dd.netlify.app").replace(/\/$/, "");
const IGNORE_TLS = process.env.BOTS_IGNORE_HTTPS_ERRORS === "1";
const RETRIES = 3;
const today = new Date().toISOString().slice(0, 10);
const reportPath = path.join(process.cwd(), "bots", "reports", `funnel-${today}.md`);

const isArr = (v) => Array.isArray(v);
/** @type {{name:string,method:string,path:string,body?:object,ok:(status:number,json:any)=>boolean,note:string}[]} */
const STEPS = [
  { name: "Home", method: "GET", path: "/", ok: (s) => s === 200, note: "landing renders" },
  { name: "Quiz page", method: "GET", path: "/quiz", ok: (s) => s === 200, note: "funnel entry" },
  { name: "Compare page", method: "GET", path: "/compare", ok: (s) => s === 200, note: "core conversion surface" },
  { name: "Advisors page", method: "GET", path: "/advisors", ok: (s) => s === 200, note: "lead funnel entry" },
  { name: "Quiz broker data", method: "GET", path: "/api/quiz/data", ok: (s, j) => s === 200 && isArr(j?.brokers) && j.brokers.length > 0, note: "brokers feed the quiz/compare" },
  { name: "Quiz scoring", method: "POST", path: "/api/quiz/score", body: { answers: ["grow"], amount: "medium", goal: "grow" }, ok: (s, j) => s === 200 && isArr(j?.results), note: "server-side match engine" },
  { name: "Wealth-stack build", method: "POST", path: "/api/wealth-stack", body: { answers: ["grow"], goal: "grow", amount: "medium" }, ok: (s, j) => s === 200 && !!j?.stack, note: "Revenue #1 builder" },
];

async function probe(ctx, step) {
  let last = { status: 0, ok: false };
  for (let i = 0; i < RETRIES; i++) {
    try {
      const res = step.method === "POST"
        ? await ctx.post(BASE + step.path, { data: step.body ?? {} })
        : await ctx.get(BASE + step.path);
      const status = res.status();
      let json = null;
      try { json = await res.json(); } catch { /* non-JSON (page GET) */ }
      if (step.ok(status, json)) return { status, ok: true };
      last = { status, ok: false };
    } catch (e) {
      last = { status: 0, ok: false, err: e instanceof Error ? e.message.split("\n")[0] : String(e) };
    }
  }
  return last; // consistently failed across retries
}

async function main() {
  await fs.mkdir(path.dirname(reportPath), { recursive: true });
  const ctx = await request.newContext({ ignoreHTTPSErrors: IGNORE_TLS, timeout: 25_000 });
  const results = [];
  for (const step of STEPS) {
    process.stdout.write(`[bots:funnel] ${step.method} ${step.path} … `);
    const r = await probe(ctx, step);
    process.stdout.write(`${r.ok ? "✓" : "✗"} ${r.status}\n`);
    results.push({ step, ...r });
  }
  await ctx.dispose();

  const failed = results.filter((r) => !r.ok);
  const lines = [
    `# Funnel uptime — ${today}`, "",
    `Target: \`${BASE}\` · non-mutating funnel steps · $0 (no LLM).`, "",
    failed.length === 0
      ? `## ✅ All ${results.length} funnel steps healthy.`
      : `## ⚠️ ${failed.length}/${results.length} funnel step(s) DOWN.`, "",
    "| Step | Endpoint | Status | OK |", "|---|---|---|:--:|",
    ...results.map((r) => `| ${r.step.name} | \`${r.step.method} ${r.step.path}\` | ${r.status || "ERR"} | ${r.ok ? "✅" : "❌"} |`),
    "",
  ];
  if (failed.length) {
    lines.push("### Failures (consistent across retries)");
    for (const f of failed) lines.push(`- **${f.step.name}** (\`${f.step.path}\`) — ${f.step.note}${f.err ? ` — ${f.err}` : ` — last status ${f.status}`}`);
    lines.push("");
  }
  lines.push("---", "_Generated by `npm run bots:funnel`._");
  await fs.writeFile(reportPath, lines.join("\n") + "\n");

  console.log(`\n[bots:funnel] Report: ${path.relative(process.cwd(), reportPath)}`);
  console.log(`[bots:funnel] ${failed.length === 0 ? "ALL HEALTHY" : failed.length + " DOWN"} (${results.length} steps)`);
  process.exit(failed.length > 0 ? 1 : 0);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch((err) => { console.error("[bots:funnel] fatal:", err); process.exit(2); });
}
