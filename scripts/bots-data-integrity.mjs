#!/usr/bin/env node
// @ts-check
/**
 * Content / data-integrity sweep (bot idea #9).
 *
 * Cross-checks the live DB for values that render as broken UX: brokers with
 * placeholder/broken logo URLs (→ 404 image requests), impossible ratings,
 * missing names/slugs, and non-https logo schemes. Catches the class behind the
 * Syfe "unsplash placeholder" broken image.
 *
 *   NEXT_PUBLIC_SUPABASE_URL=… SUPABASE_SERVICE_ROLE_KEY=… npm run bots:data
 *
 * Skips clean (exit 0) when the env isn't configured (fork/clone safe).
 * Exit 1 when a hard integrity issue is found (broken image, impossible rating,
 * missing name/slug). Cost: $0 — DB query only, no browser, no LLM.
 */
import { createClient } from "@supabase/supabase-js";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;
const today = new Date().toISOString().slice(0, 10);
const reportPath = path.join(process.cwd(), "bots", "reports", `data-integrity-${today}.md`);

function classifyBroker(b) {
  const issues = [];
  const logo = (b.logo_url || "").trim();
  if (logo && /(placeholder|unsplash\.com\/placeholder|example\.com)/i.test(logo)) issues.push("placeholder/broken logo");
  if (logo && !/^https:\/\//i.test(logo) && !logo.startsWith("/")) issues.push("non-https logo scheme");
  if (b.rating != null && (b.rating > 5 || b.rating < 0)) issues.push(`impossible rating ${b.rating}`);
  if (!b.name || !b.name.trim()) issues.push("missing name");
  if (!b.slug || !b.slug.trim()) issues.push("missing slug");
  return issues;
}

async function main() {
  if (!URL || !KEY) {
    console.log("[bots:data] NEXT_PUBLIC_SUPABASE_URL / SUPABASE_SERVICE_ROLE_KEY not set — skipping (clean).");
    process.exit(0);
  }
  await fs.mkdir(path.dirname(reportPath), { recursive: true });
  const supabase = createClient(URL, KEY, { auth: { persistSession: false } });

  const { data: brokers, error } = await supabase
    .from("brokers")
    .select("id, slug, name, logo_url, rating, status")
    .eq("status", "active");
  if (error) {
    console.error("[bots:data] query failed:", error.message);
    process.exit(2);
  }

  const flagged = [];
  let noLogo = 0;
  for (const b of brokers ?? []) {
    if (!b.logo_url || !b.logo_url.trim()) noLogo++;
    const issues = classifyBroker(b);
    if (issues.length) flagged.push({ slug: b.slug, id: b.id, issues });
  }

  const lines = [
    `# Content / data-integrity — ${today}`, "",
    `Active brokers: ${brokers?.length ?? 0} · $0 (DB query).`, "",
    flagged.length === 0
      ? `## ✅ No broken-image / impossible-value broker rows.`
      : `## ⚠️ ${flagged.length} broker row(s) with hard integrity issues.`, "",
    ...(flagged.length ? ["### Issues", ...flagged.map((f) => `- \`${f.slug}\` (#${f.id}) — ${f.issues.join("; ")}`), ""] : []),
    `> Soft: ${noLogo} active broker(s) have no logo (UI falls back to initials — content gap, not a bug).`, "",
    "---", "_Generated by `npm run bots:data`._",
  ];
  await fs.writeFile(reportPath, lines.join("\n") + "\n");
  console.log(`\n[bots:data] Report: ${path.relative(process.cwd(), reportPath)}`);
  console.log(`[bots:data] ${flagged.length === 0 ? "CLEAN" : flagged.length + " issue(s)"} · ${noLogo} no-logo (soft)`);
  process.exit(flagged.length > 0 ? 1 : 0);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch((err) => { console.error("[bots:data] fatal:", err); process.exit(2); });
}
