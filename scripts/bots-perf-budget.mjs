#!/usr/bin/env node
// @ts-check
/**
 * Performance budget sweep (bot idea #8).
 *
 * Fetches key route templates and measures cheap, deterministic perf signals —
 * server response time (TTFB-ish), transferred HTML weight, and the count of
 * <script> / stylesheet <link> tags — against budgets. Catches regressions like
 * a page ballooning in HTML size or accreting render-blocking resources, without
 * a full Lighthouse run.
 *
 *   npm run bots:perf
 *   BOTS_BASE_URL=https://my-preview.vercel.app npm run bots:perf
 *   BOTS_IGNORE_HTTPS_ERRORS=1 npm run bots:perf   # sandbox TLS-MITM proxy
 *
 * Output: bots/reports/perf-YYYY-MM-DD.md + console summary.
 * Exit: 0 within budget · 1 a route breaches a HARD budget.
 * Cost: $0 — Playwright request client, no browser, no LLM.
 */
import { request } from "playwright";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const BASE = (process.env.BOTS_BASE_URL || "https://lambent-sawine-17c3dd.netlify.app").replace(/\/$/, "");
const IGNORE_TLS = process.env.BOTS_IGNORE_HTTPS_ERRORS === "1";
const today = new Date().toISOString().slice(0, 10);
const reportPath = path.join(process.cwd(), "bots", "reports", `perf-${today}.md`);

// HARD budgets (fail the gate) — generous ceilings to catch genuine
// regressions, not micro-optimise. TTFB is wall-clock to first byte.
const BUDGET = { ttfbMs: 3000, htmlKB: 1200 };
// SOFT budget (report as a warning, don't fail): Next.js code-splits into many
// chunk <script> tags, so an absolute count is noisy — surface outliers for
// visibility without tripping CI.
const SOFT = { scripts: 100 };
const ROUTES = ["/", "/compare", "/advisors", "/best/beginners", "/super", "/crypto", "/quiz", "/broker/stake"];

async function measure(ctx, route) {
  let best = null;
  for (let i = 0; i < 2; i++) {
    try {
      const t0 = Date.now();
      const res = await ctx.get(BASE + route);
      const ttfbMs = Date.now() - t0;
      const status = res.status();
      if (status !== 200) { best = best ?? { status }; continue; }
      const body = await res.text();
      const htmlKB = Math.round(Buffer.byteLength(body, "utf8") / 1024);
      const scripts = (body.match(/<script\b/gi) || []).length;
      const styles = (body.match(/<link[^>]+rel=["']stylesheet["']/gi) || []).length;
      const cand = { status, ttfbMs, htmlKB, scripts, styles };
      // keep the fastest sample (reduces network-jitter false fails)
      if (!best || (best.ttfbMs ?? Infinity) > ttfbMs) best = cand;
    } catch { /* retry */ }
  }
  return best ?? { status: 0 };
}

async function main() {
  await fs.mkdir(path.dirname(reportPath), { recursive: true });
  const ctx = await request.newContext({ ignoreHTTPSErrors: IGNORE_TLS, timeout: 30_000 });
  const rows = [];
  for (const route of ROUTES) {
    process.stdout.write(`[bots:perf] ${route} … `);
    const m = await measure(ctx, route);
    const breaches = m.status !== 200 ? ["unreachable"] : [
      m.ttfbMs > BUDGET.ttfbMs && `ttfb ${m.ttfbMs}ms>${BUDGET.ttfbMs}`,
      m.htmlKB > BUDGET.htmlKB && `html ${m.htmlKB}KB>${BUDGET.htmlKB}`,
    ].filter(Boolean);
    const warns = m.status !== 200 ? [] : [
      m.scripts > SOFT.scripts && `scripts ${m.scripts}>${SOFT.scripts}`,
    ].filter(Boolean);
    process.stdout.write(`${breaches.length ? "✗ " + breaches.join(",") : `✓ ${m.ttfbMs}ms ${m.htmlKB}KB`}${warns.length ? " ⚠ " + warns.join(",") : ""}\n`);
    rows.push({ route, ...m, breaches, warns });
  }
  await ctx.dispose();

  const failed = rows.filter((r) => r.breaches.length > 0);
  const lines = [
    `# Performance budget — ${today}`, "",
    `Target: \`${BASE}\` · budgets: TTFB<${BUDGET.ttfbMs}ms, HTML<${BUDGET.htmlKB}KB, scripts<${BUDGET.scripts} · $0.`, "",
    failed.length === 0
      ? `## ✅ All ${rows.length} routes within budget.`
      : `## ⚠️ ${failed.length}/${rows.length} route(s) over budget.`, "",
    "| Route | Status | TTFB (ms) | HTML (KB) | Scripts | CSS | Over budget | Warn |",
    "|---|--:|--:|--:|--:|--:|---|---|",
    ...rows.map((r) => `| \`${r.route}\` | ${r.status || "ERR"} | ${r.ttfbMs ?? "–"} | ${r.htmlKB ?? "–"} | ${r.scripts ?? "–"} | ${r.styles ?? "–"} | ${r.breaches.join(", ")} | ${(r.warns || []).join(", ")} |`),
    "", "---", "_Generated by `npm run bots:perf`._",
  ];
  await fs.writeFile(reportPath, lines.join("\n") + "\n");
  console.log(`\n[bots:perf] Report: ${path.relative(process.cwd(), reportPath)}`);
  console.log(`[bots:perf] ${failed.length === 0 ? "WITHIN BUDGET" : failed.length + " over budget"} (${rows.length} checked)`);
  process.exit(failed.length > 0 ? 1 : 0);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch((err) => { console.error("[bots:perf] fatal:", err); process.exit(2); });
}
