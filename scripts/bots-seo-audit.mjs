#!/usr/bin/env node
// @ts-check
/**
 * SEO / structured-data sweep (bot idea #7).
 *
 * Fetches key routes and checks the head + JSON-LD that drives search/social/AI
 * discovery: <title>, meta description, canonical, og:title, og:image, and at
 * least one VALID application/ld+json block (parsed, not just present).
 *
 *   npm run bots:seo
 *   BOTS_BASE_URL=https://my-preview.vercel.app npm run bots:seo
 *   BOTS_IGNORE_HTTPS_ERRORS=1 npm run bots:seo   # sandbox TLS-MITM proxy
 *
 * Output: bots/reports/seo-YYYY-MM-DD.md + console summary.
 * Exit: 0 clean · 1 a route is missing a critical tag (title/canonical) or has
 *       an INVALID JSON-LD block. (Missing description/og are warnings only.)
 * Cost: $0 — Playwright request client, no browser, no LLM.
 */
import { request } from "playwright";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const BASE = (process.env.BOTS_BASE_URL || "https://lambent-sawine-17c3dd.netlify.app").replace(/\/$/, "");
const IGNORE_TLS = process.env.BOTS_IGNORE_HTTPS_ERRORS === "1";
const today = new Date().toISOString().slice(0, 10);
const reportPath = path.join(process.cwd(), "bots", "reports", `seo-${today}.md`);

// Representative high-traffic templates (one per family — homepage, pillars,
// dynamic detail, funnels, content).
const ROUTES = [
  "/", "/compare", "/advisors", "/best/beginners", "/super", "/crypto",
  "/quiz", "/foreign-investment", "/wholesale", "/broker/stake",
];

const pick = (html, re) => { const m = html.match(re); return m ? m[1].trim() : null; };

function extract(html) {
  const title = pick(html, /<title[^>]*>([^<]*)<\/title>/i);
  const description = pick(html, /<meta[^>]+name=["']description["'][^>]+content=["']([^"']*)["']/i);
  const canonical = pick(html, /<link[^>]+rel=["']canonical["'][^>]+href=["']([^"']*)["']/i);
  const ogTitle = pick(html, /<meta[^>]+property=["']og:title["'][^>]+content=["']([^"']*)["']/i);
  const ogImage = pick(html, /<meta[^>]+property=["']og:image["'][^>]+content=["']([^"']*)["']/i);
  // JSON-LD blocks — validity matters (malformed = ignored by crawlers).
  const ld = [...html.matchAll(/<script[^>]+type=["']application\/ld\+json["'][^>]*>([\s\S]*?)<\/script>/gi)].map((m) => m[1]);
  let ldCount = 0, ldInvalid = 0;
  for (const block of ld) { try { JSON.parse(block); ldCount++; } catch { ldInvalid++; } }
  return { title, description, canonical, ogTitle, ogImage, ldCount, ldInvalid };
}

async function main() {
  await fs.mkdir(path.dirname(reportPath), { recursive: true });
  const ctx = await request.newContext({ ignoreHTTPSErrors: IGNORE_TLS, timeout: 25_000 });
  const rows = [];
  for (const route of ROUTES) {
    process.stdout.write(`[bots:seo] ${route} … `);
    let html = "", status = 0;
    for (let i = 0; i < 3 && status !== 200; i++) {
      try { const res = await ctx.get(BASE + route); status = res.status(); if (status === 200) html = await res.text(); } catch { /* retry */ }
    }
    const meta = status === 200 ? extract(html) : null;
    // Critical = missing title/canonical or an invalid JSON-LD block.
    const critical = status !== 200
      ? ["unreachable"]
      : [
          !meta.title && "no <title>",
          !meta.canonical && "no canonical",
          meta.ldInvalid > 0 && `${meta.ldInvalid} invalid JSON-LD`,
        ].filter(Boolean);
    const warn = status !== 200 ? [] : [
      !meta.description && "no description",
      !meta.ogTitle && "no og:title",
      !meta.ogImage && "no og:image",
      meta.ldCount === 0 && "no JSON-LD",
    ].filter(Boolean);
    process.stdout.write(`${status} ${critical.length ? "✗ " + critical.join(",") : "✓"}\n`);
    rows.push({ route, status, meta, critical, warn });
  }
  await ctx.dispose();

  const broken = rows.filter((r) => r.critical.length > 0);
  const lines = [
    `# SEO / structured-data — ${today}`, "",
    `Target: \`${BASE}\` · $0 (no LLM).`, "",
    broken.length === 0
      ? `## ✅ All ${rows.length} routes have title + canonical + valid JSON-LD.`
      : `## ⚠️ ${broken.length}/${rows.length} route(s) with critical SEO gaps.`, "",
    "| Route | Status | Title | Canon | Desc | OG | JSON-LD | Critical |",
    "|---|--:|:--:|:--:|:--:|:--:|--:|---|",
    ...rows.map((r) => {
      const m = r.meta;
      const y = (v) => (v ? "✅" : "❌");
      return `| \`${r.route}\` | ${r.status || "ERR"} | ${m ? y(m.title) : "–"} | ${m ? y(m.canonical) : "–"} | ${m ? y(m.description) : "–"} | ${m ? y(m.ogTitle && m.ogImage) : "–"} | ${m ? (m.ldCount + (m.ldInvalid ? `/✗${m.ldInvalid}` : "")) : "–"} | ${r.critical.join(", ") || (r.warn.length ? "(warn: " + r.warn.join(", ") + ")" : "")} |`;
    }),
    "", "---", "_Generated by `npm run bots:seo`._",
  ];
  await fs.writeFile(reportPath, lines.join("\n") + "\n");
  console.log(`\n[bots:seo] Report: ${path.relative(process.cwd(), reportPath)}`);
  console.log(`[bots:seo] ${broken.length === 0 ? "CLEAN" : broken.length + " route(s) with critical gaps"} (${rows.length} checked)`);
  process.exit(broken.length > 0 ? 1 : 0);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main().catch((err) => { console.error("[bots:seo] fatal:", err); process.exit(2); });
}
