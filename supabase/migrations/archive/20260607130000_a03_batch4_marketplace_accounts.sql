-- Campaign new/edit pages (app/broker-portal/campaigns/*) INSERT and SELECT
-- templates via browser client. FOR ALL with WITH CHECK ensures brokers can
-- only INSERT/UPDATE rows stamped with their own broker_slug.
DROP POLICY IF EXISTS "broker manages own templates" ON public.campaign_templates;
CREATE POLICY "broker manages own templates"
  ON public.campaign_templates
  FOR ALL TO authenticated
  USING (
    broker_slug IN (
      SELECT ba.broker_slug
      FROM   public.broker_accounts ba
      WHERE  ba.auth_user_id = auth.uid()
    )
  )
  WITH CHECK (
    broker_slug IN (
      SELECT ba.broker_slug
      FROM   public.broker_accounts ba
      WHERE  ba.auth_user_id = auth.uid()
    )
  );

