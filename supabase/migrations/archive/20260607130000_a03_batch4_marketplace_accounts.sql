  ON public.campaign_events
  FOR ALL TO service_role
  USING (true) WITH CHECK (true);

-- Broker portal homepage reads recent clicks and today's spend via browser
-- client (app/broker-portal/page.tsx). Same subquery pattern as daily_stats.
-- Write path (allocation.ts) uses admin client — no INSERT policy needed.
DROP POLICY IF EXISTS "broker reads own events" ON public.campaign_events;
CREATE POLICY "broker reads own events"
  ON public.campaign_events
  FOR SELECT TO authenticated
