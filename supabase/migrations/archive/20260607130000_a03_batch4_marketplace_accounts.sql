-- ============================================================================
-- 1. broker_accounts
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.broker_accounts (
  id                    uuid         NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  auth_user_id          uuid         NOT NULL,
  broker_slug           text         NOT NULL,
  email                 text         NOT NULL,
  full_name             text         NOT NULL,
  company_name          text,
  role                  text         NOT NULL DEFAULT 'owner',
  status                text         NOT NULL DEFAULT 'pending',
  package_id            integer,
  package_started_at    timestamptz,
  phone                 text,
  postback_api_key      text,
  webhook_url           text,
  email_preferences     jsonb,
  invited_by            text,
  last_login_at         timestamptz,
  terms_accepted_at     timestamptz,
  terms_version         text,
  updated_at            timestamptz,
  created_at            timestamptz  DEFAULT now()
);

-- Auth-user lookup (every portal auth check) + broker_slug lookup (foreign
-- references from campaigns, wallets, events all JOIN on broker_slug).
CREATE INDEX IF NOT EXISTS idx_broker_accounts_auth_user_id
  ON public.broker_accounts (auth_user_id);
CREATE INDEX IF NOT EXISTS idx_broker_accounts_broker_slug
  ON public.broker_accounts (broker_slug);

ALTER TABLE public.broker_accounts ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.broker_accounts FORCE  ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "service_role full access" ON public.broker_accounts;
CREATE POLICY "service_role full access"
  ON public.broker_accounts
  FOR ALL TO service_role
  USING (true) WITH CHECK (true);

-- Brokers read their own account row via the browser client (portal header,
-- analytics page, marketplace terms check). No INSERT/UPDATE/DELETE: broker
-- registration + account management go through admin API routes.
DROP POLICY IF EXISTS "broker reads own account" ON public.broker_accounts;
CREATE POLICY "broker reads own account"
  ON public.broker_accounts
  FOR SELECT TO authenticated
  USING (auth_user_id = auth.uid());

-- ============================================================================
-- 2. campaign_daily_stats
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.campaign_daily_stats (
  id             bigint       GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  broker_slug    text         NOT NULL,
  campaign_id    integer      NOT NULL,
  stat_date      date         NOT NULL,
  clicks         integer      NOT NULL DEFAULT 0,
  conversions    integer      NOT NULL DEFAULT 0,
  impressions    integer      NOT NULL DEFAULT 0,
  spend_cents    integer      NOT NULL DEFAULT 0
);

-- Analytics dashboard orders by broker_slug + stat_date; 14-day window lookups.
CREATE INDEX IF NOT EXISTS idx_campaign_daily_stats_broker_date
  ON public.campaign_daily_stats (broker_slug, stat_date DESC);

ALTER TABLE public.campaign_daily_stats ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.campaign_daily_stats FORCE  ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "service_role full access" ON public.campaign_daily_stats;
CREATE POLICY "service_role full access"
  ON public.campaign_daily_stats
  FOR ALL TO service_role
  USING (true) WITH CHECK (true);

-- Broker analytics dashboard reads 14-day sparklines and trend data via
-- browser client. The subquery is safe: broker_accounts owner-isolation
-- policy (above) ensures auth.uid() maps to exactly one broker_slug.
DROP POLICY IF EXISTS "broker reads own daily stats" ON public.campaign_daily_stats;
CREATE POLICY "broker reads own daily stats"
  ON public.campaign_daily_stats
  FOR SELECT TO authenticated
  USING (
    broker_slug IN (
      SELECT ba.broker_slug
      FROM   public.broker_accounts ba
      WHERE  ba.auth_user_id = auth.uid()
    )
  );

-- ============================================================================
-- 3. campaign_events
